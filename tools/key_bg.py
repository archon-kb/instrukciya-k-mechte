#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
key_bg.py — вырезание ровного фона у картинки в прозрачность (хромакей на PIL).

ЦЕЛЬ ФАЙЛА:
    Сделать у сгенерированного слоя гор прозрачный фон: где цвет близок к фоновому
    (ровная заливка вокруг силуэта) — делаем прозрачным, сам силуэт остаётся
    непрозрачным. Нужно, чтобы слои гор перекрывали солнце и накладывались по глубине.

ЧТО НА ВХОДЕ:  путь к PNG, (опц.) цвет фона hex, порог и мягкость края.
ЧТО НА ВЫХОДЕ: PNG с альфа-каналом (RGBA) — фон прозрачный, силуэт непрозрачный.
РАЗДЕЛ: Step 3, разбор героя на слои по глубине.
ЗАВИСИМОСТИ: Pillow (PIL).
"""
from __future__ import annotations
import argparse
from PIL import Image


def hex_в_rgb(s: str) -> tuple[int, int, int]:
    s = s.lstrip("#")
    return (int(s[0:2], 16), int(s[2:4], 16), int(s[4:6], 16))


def вырезать_фон(путь_вход: str, путь_выход: str, цвет_фона: tuple[int, int, int] | None,
                 порог: int, мягкость: int) -> int:
    """Делает прозрачным всё, что близко к цвету фона. Возвращает % прозрачных пикселей."""
    картинка = Image.open(путь_вход).convert("RGBA")
    пиксели = картинка.load()
    ширина, высота = картинка.size
    # если цвет фона не задан — берём усреднённо из верхних углов (там обычно фон/небо)
    if цвет_фона is None:
        углы = [пиксели[0, 0], пиксели[ширина - 1, 0], пиксели[ширина // 2, 0]]
        цвет_фона = tuple(sum(к[i] for к in углы) // len(углы) for i in range(3))
    фон_r, фон_g, фон_b = цвет_фона
    прозрачных = 0
    for y in range(высота):
        for x in range(ширина):
            r, g, b, a = пиксели[x, y]
            расстояние = ((r - фон_r) ** 2 + (g - фон_g) ** 2 + (b - фон_b) ** 2) ** 0.5
            if расстояние <= порог:
                пиксели[x, y] = (r, g, b, 0)
                прозрачных += 1
            elif расстояние <= порог + мягкость:
                # мягкий край: плавная альфа на переходе
                доля = (расстояние - порог) / макс(мягкость, 1)
                пиксели[x, y] = (r, g, b, int(255 * доля))
    картинка.save(путь_выход)
    return round(100 * прозрачных / (ширина * высота))


def макс(a: int, b: int) -> int:
    return a if a > b else b


def main() -> None:
    p = argparse.ArgumentParser(description="Вырезать ровный фон в прозрачность")
    p.add_argument("--in", dest="вход", required=True)
    p.add_argument("--out", dest="выход", required=True)
    p.add_argument("--color", dest="цвет", default=None, help="Цвет фона hex (#ffffff). Если нет — берём из углов.")
    p.add_argument("--thresh", dest="порог", type=int, default=60, help="Порог близости к фону")
    p.add_argument("--feather", dest="мягкость", type=int, default=35, help="Ширина мягкого края")
    a = p.parse_args()
    цвет = hex_в_rgb(a.цвет) if a.цвет else None
    доля = вырезать_фон(a.вход, a.выход, цвет, a.порог, a.мягкость)
    print(f"OK: {a.выход} — прозрачно {доля}% пикселей")


if __name__ == "__main__":
    main()
